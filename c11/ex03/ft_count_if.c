int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	cpt;

	i = 0;
	cpt = 0;
	while (i < length)
	{	
		if (f(tab[i]) != 0)
			cpt++;
		i++;
	}
	return (cpt);
}
